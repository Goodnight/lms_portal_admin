package com.telecom.lms.core.model.trainclass;

public class TrainClassStudentInfoSearchForm {

	
	String trainClassId;
	String depId;
	String useSn;
	String useName;
	Integer examStatus;
	String applyWay;
	String mobile;
	
	Integer page;
	Integer max;

	public Integer getPage() {
		return page;
	}

	public void setPage(Integer page) {
		this.page = page;
	}

	public Integer getMax() {
		return max;
	}

	public void setMax(Integer max) {
		this.max = max;
	}

	public String getTrainClassId() {
		return trainClassId;
	}

	public void setTrainClassId(String trainClassId) {
		this.trainClassId = trainClassId;
	}

	public String getDepId() {
		return depId;
	}

	public void setDepId(String depId) {
		this.depId = depId;
	}

	public String getUseSn() {
		return useSn;
	}

	public void setUseSn(String useSn) {
		this.useSn = useSn;
	}

	public String getUseName() {
		return useName;
	}

	public void setUseName(String useName) {
		this.useName = useName;
	}

	public Integer getExamStatus() {
		return examStatus;
	}

	public void setExamStatus(Integer examStatus) {
		this.examStatus = examStatus;
	}

	public String getApplyWay() {
		return applyWay;
	}

	public void setApplyWay(String applyWay) {
		this.applyWay = applyWay;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	
}
